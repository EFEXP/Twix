package xyz.donot.twix.util;

import android.util.Log

fun logd(title:String,logMessage:String) {
  Log.d(title,logMessage)
}
fun loge(title:String,logMessage:String) {
  Log.e(title,logMessage)
}
fun logi(title:String,logMessage:String) {
  Log.i(title,logMessage)
}
fun logv(title:String,logMessage:String) {
  Log.v(title,logMessage)
}
fun logw(title:String,logMessage:String){
  Log.w(title,logMessage)
}
